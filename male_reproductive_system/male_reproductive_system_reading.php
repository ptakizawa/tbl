			<h3>Introduction</h3>
	
			<p>The male reproductive tract is a long tube that brings the spermatozoa from the testes to the outside of the body. The tract comprises several segments with different structures and functions. As discussed previously, spermatozoa are produced in the seminiferous tubules in the testis. Spermatozoa flow from the seminiferous tubules into the rete testis and then the ductili efferentes. The ductili efferentes merge to form the epididymis which is the site where spermatozoa gain motility. The epididymis transitions into the ductus deferens which receives secretions from the seminal gland and prostate. The ductus deferens from each side merge with the urethra.</p>
			<p><img src="male_reproductive_system/images/male_reproductive_tract_cartoon.png" alt="male reproductive tract cartoon"></p>
	
			<h3>Ductili Efferentes</h3>
	
			<p>The ductuli efferentes emerge from the dorso-superior margin of each testis. They originate from the rete testis and gradually fuse to form the ductus epididymis. The epithelium has a characteristic scalloped appearance that results from a lining that contains both cuboidal and columnar epithelial cells. A layer of smooth muscle surrounds the walls. The non-ciliated cells reabsorb testicular fluid, while the ciliated cells propel the immobile sperm to the epididymis, where they gain the ability to swim.</p>	
			<p><img src="male_reproductive_system/images/ductili_efferentes.jpg" alt="ductili efferentes"></p>
	
			<h3>Epididymis</h3>			
			<p>The epididymis is a muscular, convoluted tubule that stores spermatozoa and is the site at which they acquire their motility. It is lined by a pseudostratified epithelium whose cells contain non-motile stereocilia . These stereocilia absorb much of the excess fluid containing the spermatozoa. The epithelium of the epididymis also contains mitotic basal cells. In this section, the spermatozoa can be seen in the lumen throughout the epididymis.</p>			
			<p><img src="male_reproductive_system/images/epididymis.jpg" alt="epididymis"></p>
			
			<h3>Ductus Deferens</h3>			
			<p>The ductus deferens is another muscular tubule that carries sperm downstream from the epididymis. Its wall is thicker than that of the epididymis and contains three muscular layers: inner longitudinal, middle circular, and outer longitudinal. The epithelium of the ductus deferens is similar to that of the epididymis, with pseudostratified cells bearing stereocilia.</p>			
			<p><img src="male_reproductive_system/images/ductus_deferens.jpg" alt="ductus_deferens"></p>			
			<p>The distal portion of the ductus deferens is called the ampulla and receives secretions from the seminal vesicles. The duct is now referred to as the ejaculatory duct and ducts from each side will merge and join the urethra as it runs through prostate gland. </p>
			
			<h3>Urethra</h3>			
			<p>The urethra is lined primarily by stratified or psueodstratified columnar epithelial cells, but its opening displays a stratified squamous epithelium. Erectile tissue surrounds the urethra and contains numerous blood vessels. During an erection, the arteries dilate to fill the sinuses, which obstruct venous outflow and traps blood in the penis.
			<p><img src="male_reproductive_system/images/urethra.jpg" alt="urethra"></p>
			
			<h3>Seminal Vesicle</h3>		
			<p>Seminal vesicles are glandular sacs that produce a secretion that composes 80% of the seminal fluid and contains fructose, fibrinogen, and prostaglandins. The secretion empties via a short duct into the ampulla of the ductus deferens. The seminal vesicles appear as honeycombed saccules with thin, highly branched folds of mucosa, lined by a pseudo stratified columnar epithelium. Observe the coat of smooth muscle surrounding the saccular dilation of the gland. Its contraction expels the accumulated secretion during ejaculation.
			<p><img src="male_reproductive_system/images/seminal_vesicle.jpg" alt="seminal vesicle"></p>
			
			<h3>Prostate Gland</h3>			
			<p>The prostate is a walnut-sized conglomeration of tubulo-acinar glands that surrounds the initial segment of the urethra. This gland produces a secretory product containing citric acid and proteolytic enzymes that prevent coagulation of semen. The epithelium that lines the glands is usually columnar with numerous flattened basal cells also visible. The lumen of the glands often contain prostatic concretions that accumulate over time. Their significance is unknown but they make a useful marker for identifying the prostate. The glands are surrounded by a stroma that contains connective tissue and smooth muscle.</p>		
