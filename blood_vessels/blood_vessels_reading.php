				<h2>Introduction</h2>
				
				<p>The vascular system is a continuous network of tubes or vessels that distributes blood throughout the body and returns it the heart. An endothelium lines the inner surface of the entire network of vessels but the walls of the vessels differ depending on the size of the vessel and their position along the length of the system. The walls of large arteries and veins contain three distinct layers,tunica adventitia, tunica media and tunica intima (from outside to inside), whereas capillaries and small venules may contain only endothelia cells and the occasional support cell.</p>
				
				<p>Below we describe the structural features of the different types of vessels and how those structures faciliate the functions of the vessels. The vessels are presented in order as blood travels from the heart to the peripheral tissues and returns to the heart.
				
				<h2>Elastic Artery: Aorta Wall</h2>
				
				<p>The wall of the aorta illustrates the three layers of a blood vessel. </p>
				
				<p>The tunica intima is thin and composed of endothelial cells and their underlying supporting tissue, which includes the basement membrane and internal elastic lamina. The internal elastic lamina is composed of elastic fibers.</p>
				
				<p>The tunica media is the largest portion of the wall and is composed of elastic fibers, smooth muscle and collagenous tissue. Note the presence of large numbers of elastic fibers which makes this an elastic blood vessel. The elastic fibers allow the aorta to stretch during systole to accommodate a large volume of blood, and then contract during diastole to push the blood downstream through cardiovascular system.</p>
				
				<p>Finally, the tunica adventitia is the outermost component of the arterial wall. It contains mostly connective tissue in the form of collagen and a few small blood vessels called vasa vasorum that supplies the cells that make up the arterial wall.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/elastic_artery_wall.jpg" alt="Elastic Artery Wall">
				<figcaption>Elastic Artery Wall</figcaption>
				</figure>
				
				<h2>Muscular Artery Wall</h2>
				
				<p>Muscular arteries continue from elastic arteries and control the distribution of blood throughout the body. The tunica media of muscular arteries contains fewer elastic fibers and more smooth muscle cells than elastic arteries. Note the prominent internal elastic lamina and external elastic lamina which separates the tunica media from tunica adventitia and is also composed of elastic fibers.</p>
				
				<p>Muscular arteries control the distribution of blood to different parts of the body. The smooth muscle cells in the medial layer contract to decrease the lumen of the artery and decrease blood flow or relax to increase the diameter of the lumen and the amount of blood flow. Smooth muscle cells are also responsible for synthesizing all the protein components in the walls of blood vessels, including collagen, elastic fibers and proteoglycans.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/muscular_artery_wall.jpg" alt="Muscular Artery Wall">
				<figcaption>Muscular Artery Wall</figcaption>
				</figure>
				
				<h2>Small Muscular Artery</h2>
				
				<p>As muscular arteries branch and decrease in size, the number of layers of smooth muscle cells in the tunica media decreases. Also, the internal and external elastic laminae become much less prominent.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/small_muscular_artery.jpg" alt="Small Muscular Artery">
				<figcaption>Small Muscular Artery</figcaption>
				</figure>
				
				<h2>Arterioles</h2>
				
				<p>In arterioles, the tunica media contains only one or two layers of smooth muscle cells. Contraction of the smooth muscle cells constricts the lumen of the arteriole, increasing vascular resistance and reducing the flow of blood into capillary beds.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/arteriole.jpg" alt="Arteriole">
				<figcaption>Arteriole</figcaption>
				</figure>
				
				<h2>Arteriole, Longitudinal Section</h2>
				
				<p>This image shows an arteriole in longitudinal section. The arteriole has a single layer of smooth muscle cells. Note the orientation of the smooth muscle cells in longitudinal section versus cross section.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/arteriole_longitudinal_section.jpg" alt="Arteriole Longitudinal Section">
				<figcaption>Arteriole Longitudinal Section</figcaption>
				</figure>
				
				<h2>Capillary</h2>
				
				<p>Capillaries contain a single layer of endothelial cells and their basement membrane. The lumen of capillaries is so narrow that red blood cells have to pass in single file. The thin walls of capillaries facilitate exchange of gases and small molecules between the bloodstream and surrounding tissue. Capillaries are often wrapped with pericytes which are contractile cells that regulate the activity of endothelial cells through cell adhesion proteins and gap junctions. Capillaries come in three different structures: continuous, fenestrated, and discontinuous. These are described in detail in below.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/capillary.jpg" alt="Capillary">
				<figcaption>Capillary</figcaption>
				</figure>
				
				<h2>Continuous Capillary, Electron Micrograph</h2>
				
				<p>Endothelial cells in continuous capillaries completely enclose the lumen of the blood vessel. The only gaps are the junctions between adjacent endothelial cells where small molecules can diffuse between the bloodstream and surrounding tissue. Continuous capillaries are prominent in adipose and muscle tissue and in the brain.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/continuous_capillary.jpg" alt="Continuous Capillary">
				<figcaption>Continuous Capillary</figcaption>
				</figure>
				
				<h2>Fenestrated Capillary, Electron Micrograph</h2>
				
				<p>This electron micrograph shows a capillary with a fenestrated endothelium. These capillaries are far more permeable than those with continuous endothelial linings. Identify the endothelial cells lining the capillary, and the fenestrae, or gaps, within the cells. Barely visible is an electron-dense line known as the diaphragm that functions as a filtration barrier. Fenestrated capillaries are prominent in the kidney, intestine and endocrine glands.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/fenestrated_capillary.jpg" alt="Fenestrated Capillary">
				<figcaption>Fenestrated Capillary</figcaption>
				</figure>
				
				<h2>Discontinuous Capillary, Electron Micrograph</h2>
				
				<p>Discontinuous capillaries are the leakiest of the three types of capillaries. Similar to fenestrated capillaries, discontinuous capillaries contain gaps, but the gaps here are larger than in fenestrated endothelia and lack diaphragms. These gaps allow proteins to diffuse freely across the endothelium. Note also that there is no basement membrane beneath discontinuous endothelia. This removes another barrier to protein diffusion. Discontinuous endothelia are prominent in the liver, spleen and bone marrow and are often called sinusoids in these organs.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/discontinuous_capillary.jpg" alt="Discontinuous Capillary">
				<figcaption>Discontinuous Capillary</figcaption>
				</figure>
				
				<h2>Venule</h2>
				
				<p>This image of a venule shows several of its characteristic features. Identify its endothelium and narrow layer of smooth muscle cells. Small venules are usually surrounded by pericytes, and larger venules are surrounded by smooth muscle. Venules collect blood from the capillary beds and play a critical role in immune responses to infection as they are the sites where immune cells cross from the blood into the surrounding tissue.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/venules.jpg" alt="Venule">
				<figcaption>Venule</figcaption>
				</figure>
				
				<h2>Artery and Venule</h2>
				
				<p>In many tissues and organs, arteries and venules often run together. This image compares the structure of a venule to that of the small artery and highlights the differences. The lumens of the vessels are similar in size but the artery has a thicker medial layer with more smooth muscle. Venule, with thinner walls, are more compliant and capable of holding more blood. Consequently, arteries tend to maintain their round shape better than veins in histological sections. Veins also contain valves to prevent back flow of blood but these are infrequent and are not reliably seen in histological samples.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/small_artery_and_venule.jpg" alt="Small Artery and Venule">
				<figcaption>Small Artery and Venule</figcaption>
				</figure>
				
				<h2>Vena Cava</h2>
				
				<p>This image shows the wall of the vena cava, which is the largest vein in the body. Note the relatively thin media compared to the aorta. The media layer contains primarily smooth muscle cells and collagen with very few elastic fibers.</p>
				
				<figure>
				<img class="img-responsive" src="blood_vessels/images/vena_cava.jpg" alt="Vena Cava">
				<figcaption>Vena Cava</figcaption>
				</figure>
				
				<h2>Lymphatic Vessel</h2>
				
				<p>Lymphatic vessels are responsible for draining interstitial fluid and returning it to the bloodstream. These vessels are lined by endothelial cells and have a very thin layer of smooth muscle. Like veins, lymphatic vessels have valves that prevent back flow. Lymphatic vessels notably lack red blood cells, which help distinguish them from veins. The lymphatic system also plays an important role in generating immune responses.</p>
				
