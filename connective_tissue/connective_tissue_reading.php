		<h2>Introduction</h2>

			<p>Connective tissue is a term used to describe the tissue of mesodermal origin that that forms a matrix beneath the epithelial layer and is a connecting or supporting framework for most of the organs of the body.</p>
			
			<h2>Overview of Connective Tissue</h2>
			
			<p>In contrast to epithelia, connective tissue is sparsely populated by cells and contains an extensive extracellular matrix consisting of protein fibers, glycoproteins, and proteoglycans. The function of this type of tissue is to provide structural and mechanical support for other tissues, and to mediate the exchange of nutrients and waste between the circulation and other tissues. These tissues have two principal components, an extracellular matrix and a variety of support cells. These two components will be the focus of this lab.</p>
			
			<h2>Extracellular Matrix Components of Connective Tissues</h2>
			
			<p>The protein components of the extracellular matrix of connective tissue can be divided into three classes: collagenous fibers, elastic fibers and glycoproteins. In addition to the protein components, the extracellular matrix also contains hyaluronan which is a long chain of disaccharides.</p>
			
			<h3>Collagenous Fibers</h3>
			
			<p>Collagenous fibers consist of types I, II, or III collagen and are present in all types of connective tissue. They primarily resist tension. In electron micrographs, type I collagen has characteristic appearance of bundles of fibrils which can be seen in cross-session and longitudinally. Higher magnification of the fibrils reveals a repeated banding pattern which is produced by the spacing of the trimers in the fibril.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/collagen_fibers_em.jpg" alt="Collagen Fibers EM">
			<figcaption>Collagen Fibers EM</figcaption>
			</figure>
			
			<p>Connective tissue is often classified based on the amount and orientation of the collagenous fibers. Dense connective tissue contains a large amount of collagenous fibers that are oriented in the same direction and is found structure that resist tension in one direction, such as tendon. Dense, irregular connective tissue also contains a large number of collagenous fibers but these are oriented in multiple directions. This type of connective tissue is found in structures that need to resist tension in multiple directions, such as skin. Lastly, loose connective contains few collagenous fibers; instead, it contains more glycoproteins and cells. This is the most common type of connective tissue and is found in most organs in the body.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/connective_tissue_classification.jpg" alt="Connective Tissue Classification">
			<figcaption>Connective Tissue Classification</figcaption>
			</figure>
			
			<h3>Elastic Fibers</h3>
			
			<p>Elastic fibers stretch under tension but generate a recoil force when the tension is relaxed. Elastic fibers are prominent in the walls of large arteries where the allow the wall of the artery to stretch during systole and then generate a recoil during diastole. Elastic fibers are a composite material of the proteins elastin and fibrillin. Under the electron microscope, elastin appears as an amorphous mass lined by barely discernable microfibrils of fibrillin. Elastic fibers are stretchable because they are normally disorganized Ð stretching these fibers makes them take on an organized structure. When the tension is relaxed, the fibers return to their disorganized state.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/elastic_fibers.jpg" alt="Elastic Fibers">
			<figcaption>Elastic Fibers</figcaption>
			</figure>
			
			<h3>Proteoglycans</h3>
			
			<p>Proteoglycans are proteins which are covalently attached to long disaccharides. The disaccharide side-chains are linked to proteins in the ER and Golgi. Because the disaccharides are negatively charged, they attract sodium which draws in water to the connective tissue. The retention of water allows connective tissue to resist compression. Because proteoglycans stain poorly by H&amp;E, they are difficult to see in most histological samples.</p>
			
			<h2>Reticular Fibers</h2>
			
			<p>Reticular fibers are composed of type III collagen. Unlike the thick and coarse collagenous fibers, reticular fibers form a thin reticular network. Such networks are widespread among different tissues and form supporting frameworks in the liver, lymphoid organs, capillary endothelia, and muscle fibers.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/reticular_fibers.jpg" alt="Reticular Fibers">
			<figcaption>Reticular Fibers</figcaption>
			</figure>
			
			<h2>Cells of Connective Tissue</h2>
			
			<p>Although the connective tissue has a lower density of cells than the other tissues you will study this year, the cells of these tissues are extremely important.</p>
			
			<h3>Fibroblasts</h3>
			
			<p>Fibroblasts are by far the most common native cell type of connective tissue. The fibroblast synthesizes the collagen, elastic fibers and proteoglycans of the extracellular matrix. These cells make a large amount of protein that they secrete to build the connective tissue layer. Some fibroblasts have a contractile function; these are called myofibroblasts. This electron micrograph shows several fibroblasts. Note the large nuclei, with heterochromatin and euchromatin, as well as the abundant rough endoplasmic reticulum. Fibroblasts can be seen by light microscopy in type of loose connective tissue called areolar tissue.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/fibroblasts.jpg" alt="Fibroblasts">
			<figcaption>Fibroblasts</figcaption>
			</figure>
			
			<h3>Mast Cells</h3>
			
			<p>Mast cells are granulated cells typically found in connective tissue. These cells mediate immune responses to foreign particles. In particular, they release large amounts of histamine and enzymes in response to antigen recognition. This degranulation process is protective when foreign organisms invade the body, but is also the cause of many allergic reactions. Mast cells can be identified in connective tissue by their numerous cytoplasmic granules.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/mast_cells.jpg" alt="Mast Cells">
			<figcaption>Mast Cells</figcaption>
			</figure>
			
			<h3>Adipocytes</h3>
			
			<p>Adipocytes or white fat cells are specialized for the storage of triglyceride, and occur singly or in small groups scattered throughout the loose connective tissue. They are especially common along smaller blood vessels. When fat cells have accumulated in such abundance that they crowd out or replace cellular and fibrous elements, the accumulation is termed adipose tissue. These cells can grow up to 100 µn;m and usually contain once centrally located vacuole of lipid - the cytoplasm forms a circular ring around this vacuole, and the nucleus is compressed and displaced to the side. The function of white fat is to serve as an energy source and thermal insulator.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/adipocytes.jpg" alt="Adipocytes">
			<figcaption>Adipocytes</figcaption>
			</figure>
			
			<h3>Brown Fat Cells</h3>
			
			<p>Brown fat cells are highly specialized for temperature regulation. These cells are abundant in newborns and hibernating mammals, but are rare in adults. They have numerous, smaller lipid droplets and a large number of mitochondria, whose cytochromes impart the brown color of the tissue. The electron transport chain of these mitochondria is disrupted by an uncoupling protein, which causes the dissipation of the mitochondrial hydrogen ion gradient without ATP production. This generates heat.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/brown_fat_cells.jpg" alt="Brown Fat Cells">
			<figcaption>Brown Fat Cells</figcaption>
			</figure>
			
			<h3>Macrophage</h3>
			
			<p>Macrophages are phagocytic cells that are capable of engulfing foreign antigens and remnants of dead cells. Macrophages descend from monocytes and are found in most organs where they have different names depending on the organ. This image shows a macrophage with an irregularly shaped nucleus. The cytoplasm of the macrophage contains phagosomes and residual bodies, which are lysosomes with undigested material. On the right is a macrophage in the airway in the lung. These macrophages are called dust cells.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/macrophages.jpg" alt="Macrophages">
			<figcaption>Macrophages</figcaption>
			</figure>
			
			<h2>Cartilage</h2>
			
			<p>Cartilage is a specialized form of connective tissue produced by differentiated fibroblast-like cells called chondrocytes. It is characterized by a prominent extracellular matrix consisting of various proportions of connective tissue fibers embedded in a gel-like matrix rich in glycoproteins and hyaluronan. Chondrocytes produce all of the structural components of cartilage, including collagen, proteoglycans and glycosaminoglycans. Note the basophilia of the cytoplasm and the presence of lipid droplets.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/cartilage_chondrocytes.jpg" alt="Hyaline Cartilage">
			<figcaption>Cartilage and Chondrocytes</figcaption>
			</figure>
			
			<p>Three kinds of cartilage are classified according to the abundance of certain fibers and the characteristics of their matrix.</p>
			
			<h3>Hyaline Cartilage</h3>
			
			<p>Hyaline cartilage is the most common type of cartilage and has a matrix composed of type II collagen and chondromucoprotein, a copolymer of chondroitin sulfates A and C (a disaccharide) with protein. Its high concentration of negatively-charged sulfate groups makes it appear intensely basophilic under H&amp;E, and it often has a glassy appearance. Note the numerous chondrocytes in this image, surrounded by the cartilage they have produced. These cells have relatively small nuclei and often demonstrate lipid droplets in their cytoplasm. The spindle-shaped cells in the perichondrium can differentiate into chondroblasts that will eventually develop into chondrocytes. This cartilage is found in the nose, tracheal rings, and where the ribs join the sternum.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/hyaline_cartilage.jpg" alt="Hyaline Cartilage">
			<figcaption>Hyaline Cartilage - Toluidine Blue (left); H&E (right)</figcaption>
			</figure>
			
			<h3>Fibrocartilage</h3>
			
			<p>Fibrocartilage is distinguished by its high content and orderly arrangement of type I collagen fibers. It is typically located in regions where tendons attach to bones, the intervertebral discs, and the pubic symphysis. Numerous chondrocytes are spaced between the fibers. Note that the chondrocytes are surrounded by a matrix which helps differentiate fibrocartilage from dense connective tissue.</p>
			
			<figure>
			<img class="img-responsive" src="connective_tissue/images/fibrocartilage.jpg" alt="Fibrocartilage">
			<figcaption>Fibrocartilage</figcaption>
			</figure>
			
			<h3>Elastic Cartilage</h3>
			
			<p>Elastic cartilage is characterized by the presence of abundant elastic fibers and is quite cellular. It is made up of type II collagen and is located in the auricle of the ear and the epiglottis.</p>
			
