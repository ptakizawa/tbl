		  	<h2>Thymus</h2>
				<p>The thymus is the primary lymphoid organ engaged in the maturation of T-cells. It is most active during childhood and slowly atrophies after puberty, filling with adipose tissue.</p>
				
				<p>This is a low power view of a young thymus. Note that the gland is organized into numerous lobules. Each lobule features an outer cortex that is densely populated by lymphocytes and an inner medulla that has fewer lymphocytes (and thus is less heavily stained). Also note the loose collagenous capsule that extends into the thymus to form the interlobular septa that separate the lobules. The capsule and septa contain blood vessels, lymphatics and nerves.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/thymus.svg" type="image/svg+xml"></object>
				</figure>		
				
				<h3>Thymus Lobule</h3>
				<p>T-cells mature in distinct regions of the thymus. Progenitor T-cells, called thymocytes, enter the thymus through blood vessels in the border between the cortex and medulla. The thymocytes migrate to the cortex where they begin the maturation process. As the cells mature, they migrate toward the medulla. Immunocompetent T-cells exit the thymus via post-capillary venules or efferent lymphatics.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/thymus_lobule.svg" type="image/svg+xml"></object>
				</figure>		
				
				<h3>Thymus Cortex</h3>
				
				<p>The thymus cortex contains immature thymocytes and thymocytes undergoing maturation. The immature thymocytes are larger and found in the outer cortex; these cells actively divide and several mitotic cells can be seen in the cortex. Maturation of thymocytes in the cortex starts with the expression of specific cell surface protein (e.g. CD4, CD8 and T-cell receptor). Maturing thymocytes are found deeper in the cortex. </p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/thymus_cortex.svg" type="image/svg+xml"></object>
				</figure>	
				
				<p>In addition to immature and maturing thymocytes, the cortex also contains support cells called cortical epithelial cells that derive from ectoderm. The cortical epithelial cells play a critical role in positive selection of T-cells. They express on their cell surface class II major histocompatibility complex (MHC) bound to self-peptide. Only those maturing T-cells with a T-cell receptor that binds with sufficient affinity to the MHC-peptide complex will survive; the rest will undergo apoptosis. Macrophages in the cortex phagocytose apoptotic cells. The cortical epithelial cells can be recognized by their large, pale-staining nuclei.</p>
				
				<h3>Thymus Medulla</h3>
				
				<p>The medulla contains fewer T-cells and appears more lightly stained than the cortex. The epithelial cells are more easily seen in the medulla. In addition to T-cells and epithelial cells, the medulla contains large numbers of macrophages and dendritic cells. These cells play a critical role in negative selection of T-cells. The macrophages and dendritic cells express on their cell surface MHC with self-peptide. Those T-cells with receptors that bind too strongly to MHC:self-peptide are triggered to undergo apoptosis.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/thymus_medulla.svg" type="image/svg+xml"></object>
				</figure>	
				
				<p>One characteristic feature of the thymus medulla are Hassall corpuscles. These structures are swirls of epithelial cells that can often contain keratin. The function of Hassall corpuscles is currently unknown.</p>
				
				<h2>Lymph Node</h2>
				
				<p>Lymph nodes occur along the course of the lymphatic vessels. They filter the lymph before it drains back to the bloodstream. Lymph nodes are important sites of interaction between antigens, antigen presenting cells, and lymphocytes. Normally, they are only a few millimeters in diameter. However, when an immune response is initiated, the lymphocytes within the lymph nodes undergo activation and proliferation, causing the nodes to enlarge.</p>
				
				<h3>Cross-section of Lymph Node</h3>
				
				<p>Lymph nodes are usually bean-shaped, with an indented region known as the hilum. They are covered by a collagenous capsule that extends into the body of the node as trabeculae. The body of the lymph node is divided into an outer cortex and an inner medulla. The cortex contains a high concentration of lymphocytes while the inner medulla is less cellular.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/lymph_node_cartoon_labels.svg" type="image/svg+xml"></object>
				</figure>	
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/lymph_node.svg" type="image/svg+xml"></object>
				</figure>		
				
				<h3>Flow of Lymph</h3>
				
				<p>Lymph from the extracellular space carries antigens and antigen presenting cells, such as dendritic cells, from the tissues to the lymph nodes. The lymph enters the node at several points along the lymphatic system through afferent lymphatic vessels. These vessels pierce through the capsule and drain into the space below, known as the sub-capsular sinus. From the sub-capsular sinus, the lymph drains toward the medulla via channels called cortical sinuses. The sinuses are lined by endothelial cells. After reaching the medulla, the lymph drains into a complex network of medullary sinuses. The medullary sinuses converge at the hilum and drain into the efferent lymphatic vessels.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/lymph_node_flow.svg" type="image/svg+xml"></object>
				</figure>	
				
				<h3>Sampling of Antigen</h3>
				
				<p>Macrophages in the parenchyma of the lymph nodes sit underneath the sinuses and extend cellular processes into the sinus channels. These processes capture antigen which is then brought into the parenchyma of the lymph node where it can be sampled by lymphocytes.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/lymph_node_antigen_capture.svg" type="image/svg+xml"></object>
				</figure>	
				
				<h3>Capusle and Subcapsular Sinus</h3>
				
				<p>This is a high power view of the lymph node capsule and sub capsular sinus. Note the afferent lymphatic vessels traversing the capsule. The lymphatic vessels contain valves, which are clearly seen in this slide. The sub capsular sinus is lined by a layer of endothelial cells. Beneath the endothelial cells are macrophages that retrieve antigen from the lymph in the sub capsular sinus. These macrophages cannot be distinguished in histological images.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/lymph_node_subcapsular_sinus.svg" type="image/svg+xml"></object>
				</figure>	
				
				<h3>Lymphoid Follicles</h3>
				
				<p>In the cortex, B-lymphocytes are localized in lymphoid follicles just beneath the capsule. In absence of an active immune response, these follicles are known as primary lymphoid follicles and are difficult to distinguish histologically. When an immune response is underway, the follicles develop germinal centers that contain proliferating and maturing B-cells that are responding to antigen. Surrounding the germinal centers is the mantle zone that contains resting and memory B-cells. Follicles with germinal centers are called secondary lymphoid follicles.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/lymph_node_secondary_follicle.svg" type="image/svg+xml"></object>
				</figure>		
				
				<h3>Paracortex</h3>
				
				<p>The paracortex lies between the cortex and medulla and contains a high concentration of T-cells. Another feature of this region is the high endothelial venule, where circulating lymphocytes leave the bloodstream to enter the node. These post capillary HEVs can be distinguished by their cuboidal endothelial cells. Adhesion molecules called selections and integrins on the surfaces of HEVs and lymphocytes mediate attachment of lymphocytes to endothelial cells. In a T cell-dominant immunological response, one may observe expansions of the paracortical region.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/lymph_node_paracortex.svg" type="image/svg+xml"></object>
				</figure>		
				
				<h3>Medulla</h3>
				
				<p>The medulla contains aggregates of lymphoid tissue called medullary cords and lymphatic channels called medullary sinuses. The hilum of the lymph node is the location where blood vessels enter and exit the node. It is also where the medullary sinuses merge into efferent lymphatic vessels, which carry the lymph away from the node. This region contains macrophages and antibody-secreting plasma cells.</p>
				
				<p>The blood supply enters and leaves the lymph node at the hilum. The small arteries enter the lymph node and create a capillary network. Lymphocytes in the blood can then enter the lymph node across the walls of post capillary venules, which are also known as high endothelial venules, HEV. These HEVs merge into small veins, which then carry blood away from the node.</p>
				
				<h2>Spleen</h2>

				<p>The spleen is an organ located in the upper left quadrant of the abdomen. It serves three main functions:</p>
				
				<ul>
				<li>Activation of immunity against antigens from the blood</li>
				<li>Disposal of senescent blood cells, particularly erythrocytes</li>
				<li>Hemopoiesis in fetus (and in adults with particular diseases)</li>
				</ul>
				
				<p>Like the lymph nodes, the spleen is covered by an outer capsule that extends into the parenchyma as trabeculae. The majority of the spleen is composed of a matrix called the red pulp, which is the site of erythrocyte disposal. Embedded within the red pulp are small white nodules called the white pulp. These nodules contain the lymphocytes. </p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/spleen.svg" type="image/svg+xml"></object>
				</figure>	
				
				<h3>Blood Flow in the Spleen</h3>
				
				<p>The structure of the red and white pulp can be best appreciated by considering the organization of red and white pulp in relation to the blood vessels of the spleen.</p>
				
				<p>The splenic artery enters the spleen at the hills and branches into trabecular arteries. These arteries exit the trabeculae and split into central arteries, which run through the center of the white pulp.</p>
				
				<p>In the white pulp, the vessel is surrounded by the periarteriolar lymphoid sheath (PALS), which is made up of mostly T-cells. At internals along the border of the PALS lies the follicle, which consists mainly of B-cells. These follicles can develop into germinal centers, similar to those seen in the lymph node, when exposed to a reactive antigen. </p>
				
				<p>Blood can take two routes to traverse the red pulp. In the closed route, blood flows through capillaries and then immediately into the splenic veins. In the open route, blood flows through capillaries and then into the splenic cords which are composed of reticular fibers, reticular cells (a type of fibroblast) and macrophages. To reenter the circulatory system, red blood cells must squeeze through the discontinuous endothelial lining of the splenic sinusoids which surround the splenic cords. Old red blood cells that have become less flexible fail to enter the sinusoids and are phagocytosed by macrophages. The splenic sinuses will eventually merge with the splenic vein.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/spleen_blood_flow.svg" type="image/svg+xml"></object>
				</figure>	
				
				<h3>White Pulp</h3>
				
				<p>This section of the white pulp shows a central artery surrounded by peri-arteriolar lymphatic sheath (PALS), which is a T-cell zone. It is common to find B-cell lymphoid follicles closely associated with PALSs. The follicle here is a secondary follicle, as evidenced by the presence of a pale-staining germinal center. The germinal center is surrounded by a narrow ring of deep-stained mantle that contains resting B-cells. The marginal zone separates the follicle from the red pulp.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="immune_system/white_pulp.svg" type="image/svg+xml"></object>
				</figure>	
				
				<h3>Red Pulp</h3>
				
				<p>The red pulp is mainly filled with venous sinuses, which contain red blood cells and occasionally white blood cells. The sinuses are lined by elongated, rod-shaped endothelial cells that form a discontinuous endothelium. Slits between the endothelial cells allow viable red blood cells, which have entered red pulp through the open route, to squeeze into the sinuses from the splenic parenchyma. These sinuses eventually drain into the splenic vein. Surrounding the sinuses is the parenchyma, which contains macrophages and other immune cells. Macrophages engulf red blood cells that fail to cross the endothelium lining the sinuses.</p>
