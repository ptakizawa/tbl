		<h2>Introduction</h2>

		<p>Muscles are multicellular contractile units. They are divided into three types: </p>
		
		<ul>
		<li>Skeletal muscle</li>
		<li>Cardiac muscle</li>
		<li>Smooth muscle</li>
		</ul>
		
		<p>As you read about each type of muscle, think about the similarities and differences between them in terms of structure and function. </p>
		
		<h2>Skeletal Muscle</h2>
		
		<p>Skeletal muscle is mainly responsible for the movement of the skeleton, but is also found in organs such as the globe of the eye and the tongue. It is a voluntary muscle, and therefore under conscious control. Skeletal muscle is specialized for rapid and forceful contraction of short duration. </p>
		
		<h2>Organization of Skeletal Muscle</h2>
		
		<p>Skeletal muscle is made up of elongated cylindrical multinucleate cells, also called muscle fibers, that span the length of the muscle. Each muscle cells is surrounded by connective tissue called endomysium. Endomysium is similar in composition to basement membrane that underlies epithelia. Muscle cells are grouped together into fasciculi and surrounded by a connective tissue called perimysium that is rich in collagen fibers. Finally, several fasciculi are bundled to form the whole muscle. The whole muscle is surrounded by a connective tissue layer called the epimysium. Blood vessels and nerves travel through the epimysium, perimysium and endomysium to reach the individual muscle fibers.</p>
		
		<figure>
		<img class="img-responsive" src="muscle/images/skeletal_muscle_cross_longitudinal_sections.jpg" alt="Skeletal Muscle in Cross and Longitudinal Sections">
		<figcaption>Skeletal Muscle in Cross and Longitudinal Sections</figcaption>
		</figure>
		
		<p>Skeletal muscle cells are easily identifiable in histological samples. In cross section, skeletal muscle cells appear as large cells with their nuclei located to one side. In longitudinal section, the striations identify skeletal muscle cells. The cytoplasm of skeletal muscle cells is organized into long cables called myofibrils that span the length of the cell. Myofibrils are sectioned longitudinally into sarcomeres that give skeletal muscle cells their striated appearance</p>
		
		<h2>Sarcomere Electron Micrograph</h2>
		
		<p>Sarcomeres are the contractile unit of myofibrils. Sarcomeres are defined by Z-discs and composed of actin filaments and myosin filaments. Z-disc bind the plus ends of actin filaments which extend toward the center of the sarcomere and end at the H-band. Myosin filaments sit in the center of the sarcomere and span the length of the A-band. A-bands are darker in electron micrographs and H&amp;E-stained samples because they contain both actin filaments and myosin filaments. I-bands are lighter because they only contain actin filaments.</p>
		
		<figure>
		<img class="img-responsive" src="muscle/images/sarcomere_electron_micrograph.jpg" alt="Sarcomere Electron Micrograph">
		<figcaption>Sarcomere Electron Micrograph</figcaption>
		</figure>
		
		<p>Contraction of skeletal muscle is caused by myosin filaments pulling on actin filaments and shortening the distance between the Z-discs. Recall that myosin filaments are bipolar with the motors on opposite sides of the filament trying to move toward opposite Z-discs.</p>
		
		<p>The M-line contains proteins that crosslink adjacent myosin filaments.</p>
		
		<p>Two other important structure seen in this image are T-tubules and sarcoplasmic reticulum. T-tubules are invagination of the cell membrane that allows action potentials on the surface of a muscle cell to penetrate into its center. The sarcoplasmic reticulum is the equivalent of the endoplasmic reticulum in non-muscle cells and serves as a reservoir of calcium.</p>
		
		<h2>Types of Skeletal Muscle Cells</h2>
		
		<p>Most muscles contain a mixture of these extreme fiber types. In humans, the fiber types cannot be distinguished based on gross examination, but require specific stains or treatments to differentiate the fibers. </p>
		
		<p>Skeletal muscles are divided into two muscle fiber types. Slow-twitch (type I) muscle fibers contract more slowly and rely on aerobic metabolism. They contain large amounts of mitochondria and myoglobin, an oxygen-storage molecule. The reddish color of myoglobin is why these fibers may be referred to as red fibers. These muscles can maintain continuous contraction and are useful in activities such as the maintenance of posture.</p>
		
		<figure>
		<img class="img-responsive" src="muscle/images/skeletal_muscle_cell_types.jpg" alt="Skeletal Muscle Cell Types">
		<figcaption>Skeletal Muscle Cell Types</figcaption>
		</figure>
		
		<p>Fast-twitch (type II) muscle fibers contract more rapidly due to the presence of a faster myosin. Type II fibers can be subdivided into those that have large amounts of mitochondria and myoglobin and those that have few mitochondria and little myoglobin. The former primarily utilize aerobic respiration to generate energy, whereas the latter rely on glycolysis. The lack of myoglobin results in a paler color than the slow-twitch muscles, and fast-twitch fibers may therefore be referred to as white fibers. These muscles are important for intense but sporadic contractions; for example, those that take place in the biceps. </p>
		
		<h2>Neuromuscular Junction</h2>
		
		<p>Skeletal muscle cells are innervated by motor neurons. A motor unit is defined as the neuron and the skeletal muscle cells it supplies. Some motor neurons innervate one or a few muscle cells whereas other motor neurons can innervate hundreds of muscle cells. Motor axons terminate in a neuromuscular junction on the surface of skeletal muscle fibers.</p>
		
		<figure>
		<img class="img-responsive" src="muscle/images/neuromuscular_junction.jpg" alt="Neuromuscular Junction">
		<figcaption>Neuromuscular Junction</figcaption>
		</figure>
		
		<h2>Neuromuscular Junction Synapse</h2>
		
		<p>The neuromuscular junction is composed of a pre-synaptic axon terminal and a post-synaptic muscle cell. Upon depolarization of the axon, synaptic vesicles containing the neurotransmitter acetylcholine fuse with the membrane, releasing acetylcholine into the cleft. Acetylcholine binds to receptors on the post-synaptic membrane and causes depolarization of the muscle fiber, which leads to its contraction. Typically, one action potential in the neuron releases enough neurotransmitter to cause one contraction in the muscle fiber. </p>
		
		<figure>
		<img class="img-responsive" src="muscle/images/neuromuscular_synapse.jpg" alt="Neuromuscular Synapse">
		<figcaption>Neuromuscular Synapse</figcaption>
		</figure>
		
		<p>Note the basal lamina that surrounds the muscle cell. The basal lamina is similar to the basement membrane of epithelia. The basal lamina of skeletal muscle cells contains acetylcholinesterase which is an enzyme that digests acetylcholine. Acetylcholinesterase helps limit the duration of each contractile event.</p>
		
		<h2>Cardiac Muscle</h2>
		
		<p>Cardiac muscle shares important characteristics with skeletal muscle. Functionally, cardiac muscle produces strong contractions like skeletal muscle, and histologically, cardiac muscle cells appear striated like the skeletal muscle cells due to the presence of sarcomeres.</p>
		
		<p>The cells in cardiac muscle are much shorter than skeletal muscle cells and are arranged in series to span the length of the muscle. Cardiac muscle cells also branch at the ends to form connections with multiple adjacent cells, resulting in a complex, three-dimensional network. In contrast, skeletal muscle cells are arranged into long, parallel arrays.</p>
		
		<figure>
		<img class="img-responsive" src="muscle/images/cardiac_muscle.jpg" alt="Cardiac Muscle">
		<figcaption>Cardiac Muscle</figcaption>
		</figure>
		
		<p>Cardiac muscle cells contain one to two nuclei and located centrally within the cells. The cells are surrounded by numerous capillaries and contain an abundance of mitochondria to meet the energy demands of the cells.</p>
		
		<p>The cardiac muscle fibers are joined end to end by specialized junctional regions called the intercalated discs. The intercalated discs provide anchorage for myofibrils and allow rapid spread of contractile stimuli between cells. Such rapid spread of contraction allows the cardiac muscles to act as a functional syncytium. </p>
		
		<h2>Cardiac Muscle Electron Micrograph</h2>
		
		<p>Similar to the cytoplasm of skeletal muscle cells, the cytoplasm of cardiac muscle cells is organized into sarcomeres. Also note the abundance of mitochondria. The dark line is an intercalated disc that connects two adjacent cardiac muscle cells. The intercalated discs contain three types of membrane-to-membrane contacts: </p>
		
		<ul>
		<li>Adhering junctions that are connected to actin filaments to transmit contraction</li>
		<li>Desmosomes that connect to intermediate filaments and provide mechanical support to the entire tissue</li>
		<li>Gap junctions that allow the spread of current (ions) between adjacent cells and allow cardiac muscle to contract in a coordinated fashion</li>
		</ul>
		
		<figure>
		<img class="img-responsive" src="muscle/images/cardiac_muscle_cells_electron_micrograph.jpg" alt="Cardiac Muscle Cells Electron Micrograph">
		<figcaption>Cardiac Muscle Cells Electron Micrograph</figcaption>
		</figure>
		
		<h2>Smooth Muscle</h2>
		
		<p>Smooth muscle is found throughout the body and forms the contractile portion of the walls of the digestive tract from the middle portion of the esophagus to the internal sphincter of the anus; the walls of the respiratory tract from the trachea to the alveolar ducts; the walls of the urinary tract; and the walls of the arteries, veins, and large lymph vessels. Smooth muscle is specialized for slow and sustained contractions of low force. </p>
		
		<p>Smooth muscle fibers are elongated spindle-shaped cells with a single nucleus. The nucleus is located centrally and the sarcoplasm is filled with myosin and actin filaments. Importantly, these filaments are not arranged into sarcomeres as they are in skeletal and cardiac muscle cells.</p>
		
		<figure>
		<img class="img-responsive" src="muscle/images/smooth_muscle.jpg" alt="Smooth Muscle">
		<figcaption>Smooth Muscle</figcaption>
		</figure>
		
		<h2>Smooth Muscle Orientations</h2>
		
		<p>The arrangement of smooth muscle differs from organ to organ. Usually, groups of smooth muscle cells will be oriented in one direction to provide contractile force in that direction. This image is a cross section of the ileum. The smooth muscle in the intestine is arranged into two layers. In the layer at the top of the image (layer 1), the smooth muscle cells are arranged to contract in the direction indicated. Note the nuclei in these cells are flat and elongated. The layer at the bottom of the image (layer 2) contains smooth muscle cells that are oriented to contract into and out of the plane of the screen. Note that the nuclei of these cells are round. </p>
		
		<figure>
		<img class="img-responsive" src="muscle/images/smooth_muscle_orientation.jpg" alt="Smooth Muscle Orientation">
		<figcaption>Smooth Muscle Orientation</figcaption>
		</figure>
		
		<h2>Smooth Muscle Cell Electron Micrograph</h2>
		
		<p>The thick (myosin) and thin (actin) filaments are scattered throughout the sarcoplasm of smooth muscle cells and are attached to dense bodies on the cell membrane and within the cytoplasm. Since the contractile proteins of these cells are not arranged into myofibrils like those of skeletal and cardiac muscle, they appear smooth rather than striated. Activation of the myosin filaments pulls the dense bodies closer together causing the cell to shrink.</p>
		
