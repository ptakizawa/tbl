<h3>From single cells to multicellularity</h3>

<p>The vast majority of life forms in this world exist as single cells. In fact, most of the cells in our body function as individuals (e.g. bacteria, yeast, white blood cells). Many cells in our bodies, however, work in ensembles to perform specific functions. These ensembles of cells are often divided into four classes or tissues based on their structure, function and embryological origin.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/single_cell_and_tissue.jpg" alt="Single Cell and Group of Cells">
<figcaption>Single Cell and Group of Cells</figcaption>
</figure>

<h3>The four tissues in the human body</h3>

<p>Mammals, including humans, contain four distinct types of tissues. </p>

<ul>
<li>Epithelia</li>
<li>Muscle</li>
<li>Connective Tissue</li>
<li>Nervous</li>
</ul>
<p>Each of these tissues performs unique functions and consists of cells and a matrix of protein and sugars called the extracellular matrix. Every organ in our bodies contains a blend of these different tissues. We’ll discuss in detail the first three tissues in Scientific Foundations. Nervous tissue will be discussed in Connections to the World.</li>
</p>

<h3>Multicellularity requires several key interactions</h3>

<p>In order to function in a tissue cells must remain attached to each other. Several proteins, described below, connect cells in a tissue. These cell-to-cell attachments, however, are not sufficient to maintain the mechanical integrity of a tissue. Cells also form connections to protein components of the extracellular matrix. The proteins and sugars in the extracellular matrix are adapted to resist tension and compression.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/interactions_in_a_tissue.jpg" alt="Interaction in a Tissue">
<figcaption>Interaction in a Tissue</figcaption>
</figure>

<h3>Coordination of activity depends upon communication</h3>

<p>Besides adhering to each other and the ECM, cells in groups also communicate directly with their neighbors and the ECM. Communication allows cells to coordinate their activities and respond to external signals. This allows the body to regulate when the cells in a specific tissue are active or inactive.</p>

<h3>Cell to cell adhesion</h3>

<p>Cell to cell interactions are largely mediated by three protein complexes in the cell membrane: tight junctions, adhering junctions and desmosomes. This session will discuss adhering junctions and desmosomes. Tight junctions will be presented in the lecture on epithelia.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/cell_to_cell_adhesion_cartoon.jpg" alt="Cell to Cell Adhesion Cartoon">
<figcaption>Cell to Cell Adhesion Cartoon</figcaption>
</figure>

<p>The architecture of adhering junctions and desmosomes is similar. Both use members of the same family of proteins, called cadherins, to form interactions between adjacent cells, and both associate with the cytoskeleton within cells. Adhering junctions interact with actin filaments; desmosomes interact with intermediate filaments.</p>

<h3>Cadherins</h3>

<p>Cadherins are integral membrane proteins that mediate adhesion between cells in adhering junctions and desmosomes. Most cadherins are single transmembrane proteins that contain multiple copies of an extracellular domains called the cadherin domain. Interaction between cadherins in adjacent cells occurs through most N-terminal cadherin domain. The mechanisms by which cadherins interact is critical for maintaining cell adhesion in tissues and the integrity of tissues.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/cadherin_cartoon.jpg" alt="Cadherin Cartoon">
<figcaption>Cadherin Cartoon</figcaption>
</figure>
<h4>Cadherin structure and interactions</h4>

<p>Cadherins are a large family of proteins. All members have cadherin domains and most pass membrane once, but some span the membrane multiple times. One important feature of cadherins is that they interact homotypically, that is one type of cadherin preferentially associates with the same type of cadherin rather than a different type of cadherin. For example, E-cadherins interact with other E-cadherins but not N-cadherins.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/cadherin_family_cartoon.jpg" alt="Cadherin Family Cartoon">
<figcaption>Cadherin Family Cartoon</figcaption>
</figure>

<p>The homotypic interaction between cadherins helps tissues maintain their cellular identity. Cells in epithelia tissue express E-cadherin which allows them to associate with each other but not cells with a different type of cadherin. Likewise, cells in nervous tissue express N-cadherin but not E-cadherin, allowing cells in nervous tissue to associate while not allowing cells expressing E-cadherin to integrate into the tissue.</p>

<h4>Homotypic cadherin interaction and segregation of cells</h4>
<p>This principal of homotypic interaction of cadherins and segregation of cells can be demonstrated experimentally. Identical cells were engineered to express either E-cadherin or N-cadherin. The cells were mixed in culture and allowed to grow. Over time the cells expressing E-cadherin clustered away from the cells expressing N-cadherin which had also formed clusters. The results show that cadherins are sufficient to hold cells together to form a group and that distinct groups of cells can be formed by expressing different types of cadherins.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/homotypic_interaction_experiment_cartoon.jpg" alt="Cadherin Family Cartoon">
<figcaption>Cadherin Family Cartoon</figcaption>
</figure>

<h4>Expression level of cadherin and segregation of cells</h4>

<p>A related principle of cadherin interaction is that the strength of an association between cells can be modulated by changing the amount of cadherin in the cell membrane. Similar to the experiment above, identical cells were engineered to express cadherin, but in this experiment the cells made the same type of cadherin, and therefore, all the cells should interact with each other. Instead, the researchers changed the amount of cadherin in the cells by expressing the cadherin from a strong or weak promoter. The cells were mixed in culture and allowed to grow. As expected, the cells formed groups, but the cells that expressed a high level cadherin clustered in the center of the groups, whereas the cells expressing low amounts of cadherin localized to the periphery of the groups. Thus, even when cells express the same type of cadherin, the arrangement of those cells in a group can be altered by altering the amount of cadherin in the cell membrane.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/cadherin_expression_level_experiment_cartoon.jpg" alt="Cadherin Family Cartoon">
<figcaption>Cadherin Family Cartoon</figcaption>
</figure>

<h4>Strengthening the interaction between cadherins via the cytoskeleton</h4>

<p>The interaction between any two cadherins is weak and not nearly strong enough to maintain associations between adjacent cells. The strength of association between adhering junctions and desmosomes in adjacent cells comes from the clustering of many cadherins in one region of the cell membrane. The combined interaction of thousands of cadherins in one domain, either a adhering junction or desmosome, is sufficient to hold adjacent cells together.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/single_versus_clustered_cadherins.jpg" alt="Single Cadherin Versus Clustered Cadherins">
<figcaption>Single Cadherin Versus Clustered Cadherins</figcaption>
</figure>

<p>Recall that proteins and lipids in the cell membrane diffuse rapidly due to thermal energy, so something must hold cadherins in one regions of the cell membrane or they would end up distributed throughout the cell membrane and not able to form sufficiently strong interactions to hold cells together.</p>

<h4>Interactions with the cytoskeleton clusters cadherins</h4>

<p>The key to keeping cadherins in one region of the cell membrane is their interaction with the cytoskeleton which inhibits the diffusion of cadherins with in the cell membrane. In adhering junctions cadherins associate with actin filaments, whereas in desmosome, cadherins associate with intermediate filaments.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/cadherin_interaction_actin_intermediate_filaments.jpg" alt="Cadherin Interaction with Actin and Intermediate Filaments">
<figcaption>Cadherin Interaction with Actin and Intermediate Filaments</figcaption>
</figure>

<h3>Adhering junction</h3>

<p>In adhering junctions, a set of proteins that include alpha-catenin and beta-catenin link cadherins to actin filaments. Although the interaction between cadherins, the catenins and actin filaments may appear to serve only structural purposes, recent research has shown that beta-catenin is a transcription factor and its activity increases during several cell signaling events. We will explore the role of beta-catenin in cell signaling pathways in the session on Cell Communication.</p>

<figure>
<p><img class="img-responsive" src="../images/cells_to_tissues/adhering_junction_cartoon.jpg" alt="Adhering Junction Cartoon"></p>
<figcaption>Adhering Junction Cartoon</figcaption>
</figure>

<h3>Desmosomes</h3>

<p>Similar to the association of cadherins to actin filaments in adhering junctions, cadherins in desmosomes are linked to intermediate filaments via a set of proteins. The significance of associating with intermediate filaments rather than actin filaments is to generate greater mechanical strength in a tissue. Recall that intermediate filaments are more robust than actin filaments and allow cells to stretch without breaking when exposed to low external forces but resist large external forces. Consequently, desmosomes are prominent in cells that are exposed to strong external forces. For example, the cells in your skin contain numerous desmosomes, but cells in other parts of the body where external forces are more mild contain few if any desmosomes.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/desmosome_cartoon.jpg" alt="Desmosome Cartoon">
<figcaption>Desmosome Cartoon</figcaption>
</figure>

<h3>Cell adhesion to an extracellular matrix</h3>

<p>Although the cell-to-cell interactions generated by cadherins are sufficient to hold cells together in culture conditions, in tissues cells must also associate with an underlying matrix of proteins and carbohydrates called the extracellular matrix (ECM). The ECM provides mechanical integrity to tissues by forming a common substratum to which cells in the same tissue can attach. In addition, the ECM regulates many important cellular activities, including growth, division and differentiation.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/cell_to_ecm_cartoon.jpg" alt="Cell to ECM Cartoon">
<figcaption>Cell to ECM Cartoon</figcaption>
</figure>

<h3>Components of the extracellular matrix</h3>

<p>Many different proteins and carbohydrates compose an extracellular matrix and those components will vary depending on the tissue. However, there are several classes of proteins that are found in most extracellular matrices.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/components_of_ecm.jpg" alt="Components of ECM">
<figcaption>Components of ECM</figcaption>
</figure>

<h4>Collagen</h4>

<p>Collagen is the most abundant protein in the human body and is a major component of the ECM. Collagen represents a large class of proteins which polymerize to form fibers or networks. The details of the structure of collagen will be discussed in the lecture on Connective Tissue. Collagen serves two primary functions. Collagens that form fibers resist tension that is applied to an extracellular matrix or tissue. Collagens that form networks organize other proteins in an extracellular matrix.</p>

<h4>Elastic Fibers</h4>

<p>Similar to collagen, elastic fibers resist tension but they also generate a recoil force that returns a tissue to its original shape after tension has been removed. The structure of elastic fibers will be presented in detail in the lecture on Connective Tissue. </p>

<p>Most extracellular matrices will have a mix of collagen and elastic fibers. Those they need to strongly resist tension and not become stretched under tension will contain predominantly collagen. Those matrices that need to stretch and recoil will have more elastic fibers.</p>

<h4>Proteoglycans</h4>

<p>Proteoglycans are proteins that have large amounts of sugar side-chains. These side chains can grow to enormous lengths and many proteoglycans are more carbohydrate than protein. Proteoglycans serve several functions in an extracellular matrix. One is to resist compression. The sugar side chains are negatively charged and attract positively charged sodium. Water follows sodium into a matrix with proteoglycan and the retention of water in the matrix helps it resist compression from an external force (Think of the ease of compression a plastic bottle that is empty and without a cap versus one that is filled with water and capped).</p>

<h4>Additional components</h4>

<p>The ECM contains many other proteins than the ones listed above. These proteins help to organize the ECM and provide sites for cells to attach the ECM.</p>

<h4>Production of components of ECM</h4>
<p>How are all of these protein and proteoglycans in the extra-cellular matrix produced? Some of the components are produced by the cells within the tissue, but there is also a specialized cells called a fibroblast that is capable of making collagen, elastic fibers, proteoglycans and other proteins. We'll discuss fibroblasts in more detail in the lecture on Connective Tissue.</p>

<h3>Cell to ECM interactions</h3>

<p>Cells need a mechanism to attach to the ECM. Similar to the cell-to-cell interactions described above, cells use integral membrane proteins in the cell membrane to attach to the ECM. These proteins are called integrins.</p>

<h3>Integrins</h3>

<p>Integrins are heterodimers comprising an alpha subunit and a beta subunit. Both subunits span the cell membrane once. Our genome encodes 18 different alpha integrins and 8 different beta integrins. These subunits combine to form 24 different pairs of integrins, giving cells the ability to bind a variety of proteins in the extracellular matrix, including collagen, fibronectin and laminin. In addition, in some cells integrins mediate attachment to other cells.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/integrins_cartoon.jpg" alt="Integrins Cartoon">
<figcaption>Integrins Cartoon</figcaption>
</figure>

<p>Similar to cadherins, integrins are linked to the cytoskelelton. In most cells integrins are linked to actin filaments via a set of intermediary proteins including talin, vinculin and alpha-actinin. In cells subject to strong external force, integrins associate with intermediate filaments. These integrins form a structure called a hemi-desmosome because it resembles half of a desmosome in electron micrographs.</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/integrin_cytoskeleton_interactions_cartoon.jpg" alt="Integrin Cytoskeleton Interactions Cartoon">
<figcaption>Integrin Cytoskeleton Interactions Cartoon</figcaption>
</figure>

<h3>Communication in tissues</h3>

<p>Because most tissue perform specific functions under certain conditions, the activities of the cells in a tissue must be coordinated. In addition, the growth and division of cells in a tissue must be regulated to ensure that cells don't proliferate too much</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/communication_in_tissues.jpg" alt="Communication in Tissues">
<figcaption>Communication in Tissues</figcaption>
</figure>

<p>There are three primary mechanisms through which cells communicate and tissues regulate the activity of cells: gap junctions, diffusible signaling molecules, and via cell-to-cell or cell-to-ECM interactions.</p>

<h4>Gap junctions</h4>

<p>Gap junctions are a collection of protein pores in the cell membrane that interconnect adjacent cells. The individual pores of gap junctions are size restrictive: molecules smaller than 1000 Da can freely diffuse through pores while larger molecules are prevented from passing. Thus, gap junctions allow ions and other small molecules (e.g. ATP) to diffuse freely between the cytoplasm of adjacent cells. Because ions and small molecules are often involved in triggering signaling reactions within cells, gap junctions are effective means for a signaling event at one cell to pass throughout a collection of cells</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/gap_junctions_cartoon.jpg" alt="Gap Junctions Cartoon">
<figcaption>Gap Junctions Cartoon</figcaption>
</figure>

<h4>Connexins</h4>

<p>Connexins are proteins that make up the pores in gap junctions. 6 connexins assemble in the cell membrane of one cell to form a pore that interacts with a complex of 6 connexins in neighboring cells to form a continuous channel between the two cells. Connexins are a large family of proteins some of which have been associated with diseases (e.g. connexin 26 and deafness).</p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/connexins_cartoon.jpg" alt="Connexins Cartoon">
<figcaption>Connexins Cartoon</figcaption>
</figure>

<h4>Diffusible signaling molecules</h4>

<p>In any tissue, cells are bathed in a variety of molecules that regulate the activity of those cells. These molecules can be produced locally by cells within the tissue or by cells in another region of the body. Many factors impact how these signaling molecules affect the activity of cells within a tissue, and the details of how these molecules change the behavior of cells will be discussed in the session on Cell Communication. </p>

<figure>
<img class="img-responsive" src="../images/cells_to_tissues/diffusible_signaling_molecules.jpg" alt="Diffusible Signalin Molecules">
<figcaption>Diffusible Signaling Molecules</figcaption>
</figure>

<p>One key factor in cell signaling is the concentration of the signaling molecule that surrounds the cells. Higher concentrations of a signaling molecule are more likely to trigger a change in the activity of cells. In many tissues, components of the ECM regulate the concentration of signaling molecules by binding signaling molecules and preventing them from interacting with the cells in the tissue.</p>

<h4>Communication via cell-to-cell and cell-to-ECM interactions</h4>

<p>Besides maintaining the physical integrity of tissues, cell-to-cell and cell-to-ECM interactions also affect the behavior of cells. For example, interactions between cadherins in adhering junctions is known to inhibit the proliferation of cells. This behavior is often referred to as contact inhibition in which normal cells in culture will grow and divide until they make contact via cadherins with another cell.</p>

<p>The interaction between integrins and the extracellular matrix also affects the behavior of cells, including their growth and division, shape and motility (yes, some of our cells can crawl). In many cells, integrins cluster into small domains within the cell membrane called focal adhesions. These clusters serve as platforms for several different signaling pathways. Exactly how the interactions affect the behavior of the cells depends upon the type of integrin and how tightly the integrins are bound to the extracellular matrix. </p>

