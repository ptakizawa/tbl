				</figure>
				
				<h3>Collecting Ducts</h3>
				<p>The terminal portion of the distal tubule empties through collecting tubules into a straight collecting duct. The collecting duct system is under the control of antidiuretic hormone (ADH). When ADH is present, the collecting duct becomes permeable to water. The high osmotic pressure in the medulla (generated by the counter-current multiplier system/loop of Henle) then draws out water from the renal tubule, back into the surrounding blood vessels. ADH causes the epithelial cells that line collecting ducts to increase the number of aquaporin 2 channels in their apical plasma membrane. Aquoporins allow the diffusion of water across the plasma membrane. In the absence of ADH, aquaporin 2 localizes to storage vesicles in the cytoplasm of collecting duct epithelial cells. ADH stimulates the transport of aquaporin 2 from storage vesicles to the plasma membrane.</p> 
				<p>Collecting ducts can be differentiated from other tubules by the prominent lateral borders of the epithelial cells.</p>
