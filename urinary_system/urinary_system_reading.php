				<h3>Podocytes</h3>
				<p>Podocytes are specialized epithelial cells that separate the network of capillaries in the glomerulus from Bowmanâ€™s space. Podocytes extend processes that surround the capillaries. These processes form secondary processes called foot processes. The foot processes associate with the basement membrane opposite from the endothelial cells of the capillaries.</p>
