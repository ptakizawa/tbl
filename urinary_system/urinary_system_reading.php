				<h3>Distal Convoluted Tubule</h3> 
				<p>The distal convoluted tubule follows the loop of Henle. Further reabsorption and secretion of ions occur in this segment.  The initial segment of the distal convoluted tubule lies right next to the glomerulus and forms the juxtaglomerular apparatus.</p> 
				<p>The cells of the distal convoluted tubule are smaller and more lightly stained than those of the proximal convoluted tubule. Consequently, more nuclei are apparent in a cross section of distal convoluted tubule compared to proximal convoluted tubule. Distal convoluted tubules also lack a brush border on their apical surface. Note that in any given section of the kidney cortex, much less space is occupied by distal convoluted tubules as compared to proximal convoluted tubules. This is simply because the distal convoluted tubule is shorter and less convoluted.</p>
				 <figure style="width:100%;">
					<object height="100%" width="100%" data ="urinary_system/distal_convoluted_tubule.svg" type="image/svg+xml"></object>
				</figure>
				
				<h3>Juxtaglomerular Apparatus</h3> 
				<p>The juxtaglomerular apparatus is a specialized structure formed by the distal convoluted tubule and the glomerular afferent arteriole near the vascular pole of the glomerulus. Its main functions are to regulate blood pressure and the filtration rate of the glomerulus. The complex secretes renin, which regulates systemic blood pressure via the renin-angiotensin-alodosterone system. The complex also regulates pressure in the afferent arteriole in response to changes in the rate of filtration. The complex is composed of the following cells:
				<ul>
					<li>The macula densa is a collection of specialized epithelial cells of the distal convoluted tubule. These cells are enlarged as compared to surrounding tubular cells. The cells of the macula densa sense sodium chloride concentration in the tubule which is an indicator of glomerular filtration rate in the nephron. In response to elevated sodium, the macula dense cells trigger contraction of the afferent arteriole, reducing flow of blood to the glomerulus and the glomerular filtration rate.</li>
					<li>The juxtaglomerular cells are part of the afferent arterioles, which are responsible for secreting renin. Renin increases blood pressure via the renin-angiotensin-aldosterone system. Juxtaglomerular cells cells are derived from smooth muscles cells of afferent arterioles.</li>
					<li>The Lacis cells, also called extraglomerular mesangial cells, are flat and elongated cells located near the macula densa. Their function is currently unclear. </li>
				</ul>
