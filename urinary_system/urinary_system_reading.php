				<h3>Renal Corpuscle</h3>
				<p>The renal corpuscle is responsible for the filtration of blood. It contains two structures: the glormerulus and Bowman's capsule. The glomerulus is a cluster of capillaries surrounded by a thick basement membrane and a layer of epithelial cells called podocytes. Blood flows into the capillaries from an afferent arteriole and leaves the corpuscle via an efferent arteriole. Blood in the efferent arteriole enters a second capillary bed that perfuses the region of the kidney that house the nephron tubules.</p>
				<p>The hydrostatic pressure in the glomerular capillaries is significantly higher than oncotic pressure which pushes fluid out of the capillaries into Bowman's space and the rest of the nephron. The capillaries contain a fenestrated endothelia to facilitate passage of fluid and are surrounded by a thick basement membrane and a layer of epithelial cells called podocytes. As discussed below, the endothelial cell, basement membrane and podocyte define the filtration barrier of the glomerulus.</p>
				<p>Bowman's capsule surrounds the glomerulus and is defined by a layer of epithelial cells that are continuous with the podocytes that surround the capillaries. The capsule is composed of a layer of epithelial cells.</p>
				<p>Mesangial cells are also found in the glomerulus, and they secrete the proteins and glycoproteins that compose the matrix that supports the other cells in the glomerulus.</p>
