	        	<h2>Introduction</h2>
	        	<p>The urinary system comprises the kidney, ureter, urinary bladder, and urethra.  The kidney filters blood to produce urine, which contains excess water, electrolytes and waste products of the body. Urine flows from the kidney through the ureter into the bladder where it is temporarily stored. The bladder is then emptied via the urethra.</p>
	        	<h3>The Kidney</h3>
	        	<p>The kidney performs several important functions:</p>
	        	<ul>
					<li>Maintenance of water and electrolyte homeostasis</li> 
					<li>Regulation of acid-base balance in conjunction with the respiratory system</li> 
					<li>Excretion of metabolic waste products, especially the toxic nitrogenous compounds</li> 
					<li>Production of renin for blood pressure control and erythropoietin, which stimulates red blood cell production in the bone marrow</li> 
					<li>Conversion of vitamin D into active form for the regulation of calcium balance</li>
	        	</ul> 
				<p>The kidney performs many of the above functions by filtering blood to remove ions and small molecules and then selectively reabsorbing ions and small molecules depending on the current conditions of the body. The main functional unit of the kidney is the nephron which combines a filtration unit (corpuscle) and a long tube lined by epithelial cells that vary along its length in their properties of reabsorption and passive diffusion. The segments of the tube are divided into proximal convoluted tubule, loop of Henle and distal convoluted tubule. Each nephron is fed by an artery (afferent) that supplies blood to be filtered. The nephron can control the rate at which it filters blood by dilating or constricting the afferent artery.</p>
