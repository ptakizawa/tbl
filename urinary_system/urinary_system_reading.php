				</figure>
				
				<h3>Arrangement of Corpuscles and Tubules</h3>
				<p>This is a low power view of a cross section through the kidney shows the outer cortex and inner medulla of the kidney. The cortical section contains corpuscles, tubules, and blood vessels. The medullary primarily contains loops of Henle, collecting ducts and blood vessels. Note the large blood vessels at the border of the cortex and medulla. These are the arcuate arteries and are often used to distinguish cortex from medulla.</p>
				
