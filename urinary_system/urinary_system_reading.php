				<h3>Filtration Barrier</h3>
				<p>The filtration barrier of the glomerulus is a size and charge selective barrier. The structural components of the barrier start with a fenestrated capillary epithelium with a negatively charged surface that repels many plasma proteins. Beneath the capillary epithelium is a thick basement membrane that is also negatively charged. The foot processes of the pododcytes form the final barrier. Note the slit diaphragm between the foot processes. These play a role in filtration of plasma as mutations in genes that encode proteins of slit diaphragms lead to proteinuria. The strong negative charge on the surface of the endothlelium and within the basement membrane repel negatively charged proteins in plasma, notably albumin.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="urinary_system/filtration_barrier.svg" type="image/svg+xml"></object>
				</figure>
				
				<h3>Proximal Convoluted Tubule</h3>	
				<p>The proximal convoluted tubule is the first and longest segment of renal tubule and begins at the urinary pole of the glomerulus. The proximal convoluted tubule is the site where the majority (65%) of ions and water in the urinary space is reabsorbed back into the body. The cells of the proximal convoluted tubule have a deeply stained, eosinophilic cytoplasm. The cells are large so that in cross section not every nucleus will be visible, making it appear that the proximal convoluted tubule has fewer nuclei than other tubules. The cells also have an apical brush border to increase their surface area. The convoluted portion of the tubule leads into a straight segment that descends into the medulla within a medullary ray and becomes the loop of Henle.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="urinary_system/proximal_convoluted_tubule.svg" type="image/svg+xml"></object>
				</figure>
				
				<p>This is an electron micrograph of the proximal convoluted tubule. Note the microvilli forming the brush border on the apical surface. The basal striations in these cells are due to the parallel alignment of mitochondria alongside the basolateral regions of the plasma membrane. These mitochondria support active ion transport by sodium-potassium pumps on the basal plasma membrane by providing an abundant amount of ATP. The ion gradient created by these pumps in turn supports ion and water reabsorption on the apical side of the proximal convoluted tubule cells. Basal striations with mitochondria and sodium-potassium pumps are also found in cells in the distal convoluted tubules.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="urinary_system/proximal_convoluted_tubule_em.svg" type="image/svg+xml"></object>
				</figure>
				
				<h3>Loop of Henle</h3>
				<p>The loop of Henle forms a hair-pin structure that dips down into the medulla. It contains four segments: the thick descending limb, which extends from the proximal convoluted tubule, followed by the thin descending limb, the thin ascending limb, and the thick ascending limb. The turn of the loop of Henle usually occurs in the thin segment within the medulla, and the tubule then ascends toward the cortex parallel to the descending limb. The end of the loop of Henle becomes the distal convoluted tubule near its original glomerulus. The loops of Henle run in parallel to capillary loops known as the vasa recta. Recall from Physiology that the loop of Henle serves to create high osmotic pressure in the renal medulla via the counter-current multiplier system. Such high osmotic pressure is important for the reabsorption of water in the later segments of the renal tubule.</p> 

<p>Periodic acid-Schiff Staining (PAS). The thick descending limbs of the loop of Henle look similar to the proximal tubule, with apical brush borders. The thick ascending limbs are composed of cuboidal cells, but unlike the proximal convoluted tubule, they do not have apical brush borders. Collecting ducts can also be seen on this slide. They can be easily distinguished by the presence of prominent lateral borders between adjacent cells. </p>
				<figure style="width:100%;">
