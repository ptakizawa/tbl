				<h3>Renal Pelvis and Ureter</h3> 
				<p>Numerous collecting ducts merge into the renal pelvis, which then becomes the ureter. The ureter connects the kidney and the urinary bladder.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="urinary_system/renal_pelvis.svg" type="image/svg+xml"></object>
				</figure>
				
				<p>The ureter is a muscular tube, composed of an inner longitudinal layer and an outer circular layer of smooth muscle. The lumen of the ureter is covered by transitional epithelium (also called urothelium). Recall that the transitional epithelium is unique to the conducting passages of the urinary system. Its ability to stretch allows the dilation of the conducting passages when necessary.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="urinary_system/ureter.svg" type="image/svg+xml"></object>
				</figure>
				
				<h3>Urinary Bladder</h3> 
				<p>The ureter empties the urine into the bladder. The transitional epithelium continues over the surface of this organ. The thickened muscular layers become interwoven and cannot be clearly identified at this point.</p> 
				<p>The urinary bladder is lined by transitional epithelium, underneath which are thick layers of smooth muscle interwoven in various directions. This image shows a relaxed bladder where the epithelial cells appear cuboidal. In a distended bladder the epithelial cells are stretched and become more squamous.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="urinary_system/bladder.svg" type="image/svg+xml"></object>
				</figure>
				 
				<h3>Urethra</h3> 
				<p>The urethra carries the urine away from the bladder to the outside of the body. In the male, it is joined by the genital system. The epithelium changes from transitional to stratified or pseudostratified columnar in the urethra, and to stratified squamous in the distal end of the urethra.</p>
													
			  
				
				
