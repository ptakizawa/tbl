	          <h2>Introduction</h2>
				
				<p>The skeletal system, which is made up of bone and cartilage, serves several functions: </p>
				
				<ul>
				<li>Mechanical support for sites of muscle attachment</li>
				<li>Protection of vital organs</li>
				<li>Reservoir for calcium and phosphate</li>
				<li>Site for development of blood cells</li>
				</ul>
				
				<h2>Macroscopic Organization of Bone</h2>
				
				<p>Bone comes in two general architectures. Compact bone which composes the outer wall of most bones and trabecular bone which is found in the inner cavities of bone. A long bone illustrates both types of bone. Trabecular bone is found in the head region or epiphysis of the bone and appears as many struts or spicules of bone. A thick layer of compact bone is found along the shaft or diaphysis of the long bone.</p>
				
				<figure>
				<img class="img-responsive" src="bone/images/long_bone.jpg" alt="Long Bone">
				<figcaption>Long Bone</figcaption>
				</figure>
				
				<p>Long bone also illustrates the two surface of bone. The periosteal or outer surface of the bone is normally covered with a layer of fibrous tissue called periosteum and is the site where muscle and tendon attaches to bone. The inner surface bone is called the endosteal surface and is usually covered by a thin layer of cells called the endosteum.</p>
				
				<p>All bone is composed of type I collagen fibrils that are encased in crystals of calcium-phosphate.</p>
				
				<h2>Trabecular Bone</h2>
				
				<p>This image illustrates trabecular bone. Note the thin, branched spicules of bone. Within the bone matrix are osteocytes. Around the trbeculae is bone marrow.</p>
				
				<figure>
				<img class="img-responsive" src="bone/images/trabecular_bone.jpg" alt="Trabecular Bone">
				<figcaption>Trabecular Bone</figcaption>
				</figure>
				
				<h2>Compact Bone</h2>
				
				<p>Compact bone is organized into parallel columns, known as Haversian systems, which run lengthwise down the axis of long bones. This image shows compact bone in cross section. Haversian systems comprise concentric rings of bone around a central channel or Haversian canal. The canal contains nerves, blood vessels, and lymphatic system of the bone. Also seen in this image is a Volkmann's canal. These canals start at the periosteal surface and contain blood vessels that connect to blood vessels in the Haversian canals. This image and the next are an example of unstained bone.</p>
				<figure>
				<img class="img-responsive" src="bone/images/compact_bone.jpg" alt="Compact Bone"></p>
				<figcaption>Compact Bone - Ground Section, unstained</figcaption>
				</figure>
				
				<h2>Haversian System</h2>
				
				<p>Osteoblasts synthesize the concentric rings of bone in the Haversian systems. As osteoblasts secrete matrix, they become trapped in spaces called lacunae and become known as osteocytes. Osteocytes communicate with each other and with the Haversian canal through cytoplasmic extensions called filopodia that run through small interconnecting canals in the bone called canaliculi. Filopodia from adjacent osteocytes communicate via gap junctions.</p>
				
				<figure>
				<img class="img-responsive" src="bone/images/haversian_system.jpg" alt="Haverisan System">
				<figcaption>Haverisan System - Ground Section, unstained</figcaption>
				</figure>
				
				<h2>Osteoblasts</h2>
				
				<p>Osteoblasts synthesize new bone by secreting collagen fibrils and facilitating the mineralization of the fibrils. Active osteoblasts sit atop a layer of osteoid that contains collagen fibrils that have not completely mineralized. Over time the fibrils in the osteoid with mineralize to form bone.</p>
				
				<figure>
				<img class="img-responsive" src="bone/images/osteoblast.jpg" alt="Osteoblast">
				<figcaption>Osteoblast</figcaption>
				</figure>
				
				<h2>Osteoclasts</h2>
				
				<p>Osteoclasts are large, multinucleated cells that derive from monocytes and therefore are related to macrophages. They resorb bone by secreting organic acids, which dissolve hydroxyapatite, and enzymes, which digest the collagen fibrils. Active osteoclasts create surface depressions in bone due to reposition of the bone.</p>
				
				<figure>
				<img class="img-responsive" src="bone/images/osteoclast.jpg" alt="Osteoclast">
				<figcaption>Osteoclast</figcaption>
				</figure>
				
				<h2>Activation of Osteoclasts</h2>
				<p>Recall from lecture that osteoclasts derive from monocytes that differentiate into pre-osteoclasts. Pre-osteoclasts transform into osteoclasts when they bind RANK ligand on the surface of osteoblasts. When the body needs more calcium, it produces parathyroid hormone which increases the the number of osteoclasts.</p>
				
				<figure>
				<img class="img-responsive" src="bone/images/osteoclast_activation.jpg" alt="Osteoclast Activation">
				<figcaption>Osteoclast Activation</figcaption>
				</figure>
				
				<h2>Bone Modeling and Remodeling</h2>
				
				<p>Osteoclasts and osteoblasts often work together to reshape bone (modeling) and replace old bone with new bone (remodeling). In bone modeling the osteoclasts and osteoblasts work on opposite surfaces of bone to position the bone in one direction. In bone remodeling, osteoclasts and osteoblasts work on the same surface of bone to digest old bone and replace it with new bone. Proper balance between the number and activity of osteoblasts and osteoclasts is essential to ensure that bone is neither overproduced nor over degraded.</p>
				
				<figure>
				<img class="img-responsive" src="bone/images/modeling_remodeling.jpg" alt="Bone Modeling and Remodeling">
				<figcaption>Bone Modeling and Remodeling</figcaption>
				</figure>
				
				<h2>Development of Bone</h2>
				
				<p>During development, bone can generally only form on a preexisting structures. The two most common structures are mesenchymal tissue and cartilage. Bone forming on mesenchyme is called intramembraneous ossification, whereas bone forming on cartilage is called endochondrial ossification. These are important not only during development, but the same mechanisms are used to repair bone when it fractures.</p>
				
				<h3>Intramembraneous Ossification</h3>
				
				<p>Intramembranous ossification is the direct conversion of embryological mesenchymal tissue to bone. The process begins when mesencyhmal cells differentiate into osteoblasts, which begin to synthesize osteoid that will eventually mineralize into bone.</p>
				
				<figure>
				<img class="img-responsive" src="bone/images/intramembraneous_ossification.jpg" alt="Intramembraneous Ossifcation">
				<figcaption>Intramembraneous Ossifcation</figcaption>
				</figure>
				
				<h3>Endochondrial Ossification</h3>
				
				<p>In endochondrial ossification, bone is synthesized over a cartilage template. This image shows a growing tibia. The purple growth plate is composed of cartilage synthesized by the embedded chondrocytes. Over time, the cartilage becomes calcified; the dark purple areas within the trabecuale are remnants of calcified cartilage. As the cartilage emerges from the growth plate, woven bone, which appears light blue in this slide, is laid down over the calcified cartilage. This preliminary bone will eventually be replaced through bone remodeling to produce more organized lamellar bone. Osteoid, appearing red, can be seen laid down over the primary trabeculae of woven bone with a cartilage core.</p>
				
				<figure>
				<img class="img-responsive" src="bone/images/endochondrial_ossification.jpg" alt="Endochondrial Ossifcation">
				<figcaption>Endochondrial Ossifcation</figcaption>
				</figure>
				
				<h2>Chondrocyte Growth Sequence</h2>
				
				<p>This image shows the different stages of chondrocyte development during endochondrial ossification. In the initial stage, the chondrocytes are resting and the cartilage is not being converted to bone matrix. They then go through a stage of proliferation, where each lacuna contains numerous chondrocytes. This is followed by maturation/hypertrophy phases in which the chondrocytes and their lacunae appear much larger than when they were resting. The cartilage is eventually calcified. Finally, the calcified cartilage is adsorbed by osteoclasts, woven bone is laid down and after ~70 days is converted to lamellar bone. </p>
				
