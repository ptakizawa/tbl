		         	<h1>The Student Experience in TBL</h1>
					 <p>Team-based learning is a unique opportunity for you to learn content and use your recently gained knowledge to solve real-world problems while developing skill in critical thinking and teamwork. Team-based learning divides the class into teams. Each team has 6 or 7 members and assignments to a team are made randomly and persist for the entire pre-clinical curriculum. Most of the work you do in class will be with your teammates. To gain the most educational value from your experience in team-based learning, we suggest you read the following guidelines before your first session.</p>

					 <h2>Pre-class Activities</h2>

					 <p>Team-based learning employs a flipped curriculum in which you will learn content before coming to class. This content will be delivered as readings and/or videos and will be available at least a week prior to the in-class session. To achieve best educational experience from a TBL session, it’s critical that you complete all of the pre-session readings and videos as the in-class session will require you to use the pre-session content to answer questions and solve problems.</p>

					 <h2>In-class Activities</h2>

					 <p>The in-class portion of a TBL session consists of two activities: readiness assessment quiz and application questions.</p>

					 <h3>Readiness Assessment Quiz</h3>

					 <p>This is a short, multiple-choice quiz that you will take at the beginning of every TBL session. The quiz is designed to assess your recall and understanding of the pre-session content. You will first take the quiz as an individual and then work on the quiz with your teammates. You should discuss the questions with your teammates and help your team arrive at a consensus answer. A scratch-off card will reveal if your team’s answer is correct. If your team’s answer is not correct, the team should select another option until it arrives at the correct answer.</p>

					 <p>After all of the teams have finished the quiz, the facilitator of the TBL session will discuss each of the questions. This is your opportunity to clear up any confusion about the question or the content the question addresses. Don’t be shy; it’s important that you understand the content because you will most likely need to use that knowledge to solve the application questions.</p>

					 <h3>Application Questions</h3>

					 <p>Application questions challenge teams with real-world problems. Students use the content they learned in the pre-session, critical analysis of that content and discussion with teammates to answer these questions. The questions are multiple-choice and often more than one option to a question is reasonable. Students are asked to develop a logical argument to support their team’s selection. A class-wide discussion after each question each team to present its arguments and debate the merits of other teams’ arguments.</p>

					 <p>Importantly, the process of arriving at an answer is just as important as selecting the correct answer. Critical analysis of content, open discussion with teammates and presentation of your team’s position are invaluable skills that will benefit you in the rest of your education and medical career.</p>			
