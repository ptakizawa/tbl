		         	<h1>Introduction to Team-Based Learning</h1>
				  	<h2>What is team-based learning?</h2>
      <p>Team-based learning is a teaching method that allows one instructor to engage a large group of students through active learning and application of knowledge. Team-based learning employs a flipped curriculum that delivers content to students before class, freeing in-class time for problem-solving, critical thinking and discussion of content. Team-based learning divides a large group of students into smaller teams to facilitate student participation, promote discussion and collaboration.</p>

      <h2>Structure of a team-based learning session</h2>
      <p>A team-based learning session comprises three components: pre-session content, readiness assessment and application questions.</p>
       <hr />
      
      <h3>Component 1: Pre-session Content</h3>
      <p>Here students learn the content and skills which they will use to answer questions and solve problems during the in-class session. The material for this component can be delivered in a variety of formats, including video, readings, website or online slideshow. Students should complete this section before coming to class.</p>
      
      
      <h3>Component 2: Readiness Assessment</h3>
      <p>Here students take a short, multiple-choice quiz to assess their understanding of the pre-session content. Students first complete the quiz as individuals relying only on their recall of the pre-session content. This section is called individual readiness assessment or IRAT. After students have completed the quiz, they answer the same questions in their team. Students discuss each question with their teammates and decide on which answer to the question they think is correct. Teams receive feedback on their selection thought scratch-off cards which reveal the correct answer to each question. This section is called group readiness assessment or GRAT.</p>
      <p>After teams have finished the quiz, the instructor holds a discussion of the questions to clear up misunderstandings or confusion about the content covered in the quiz.</p> 
      
      
      <figure>
        <figcaption>Readiness Assessment Scratch Off Card</figcaption>
