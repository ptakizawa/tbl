      <h3>Component 3: Application Questions</h3>
      <p>The application questions require students to apply what they learned in the pre-session material to solve a real-world problem. Students will work on and discuss these problems in their teams and the teams should come to a consensus answer. Application questions should be significantly more challenging than the questions on the readiness assessment quiz. </p>
      <p>Besides being more challenging than the questions on the readiness assessment quiz, the application questions should also present a real-world problem to the teams. The questions should put the students in situations that they might encounter in their future careers (e.g. physician, scientist, etc.). In addition, as in the real world, students should have to come to a final decision when answering the application questions. </p>
      <p>The teams show their commitment to an answer by holding up a card that displays their choice. After all teams have committed to an answer, the facilitator leads a discussion of the question and the choices. Teams are given the opportunity to explain their rationale for selecting a particular response and present arguments why their selection is the best choice. The discussion is also a time when the facilitator can clear up any misunderstanding of the material and its applications and/or provide additional examples of how the material can be applied.</p>
