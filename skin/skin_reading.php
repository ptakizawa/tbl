			<h2>Special Structures of Skin</h2>
				<p>Skin contains a number of important functional structures that derive from epithelial cells in the epidermis. These include hair and different types of sweat glands.
				<h3>Hair</h3>
				<p>Hair is present over most of the body and plays an important role in regulating body temperature. Hair is composed of concentric layers of epithelial cells that undergo different degrees of keratinaztion. The hair follicle is where hair production and growth of occur. The hair bulb contains stems cells that will differentiate into the different layers of epithelial cells that make up hair.</p>
				<figure style="width:100%;">
						<object height="100%" width="100%" data ="skin/hair.svg" type="image/svg+xml"></object>
				</figure>
				
				<h3>Eccrine Sweat Gland</h3>
				<p>Eccrine sweat glands occur throughout most of the skin. They arise from downgrowth of the epidermis into the dermis to form of long tubules that lead to secretory glands. The secretory portion of each gland is tightly wound and appears as a collection of cross-sectioned tubules. It consists of of secretory cells and an outer layer of myoepithelial cells that are contractile. The myoepithelial cells receive input from nerve fibers, which cause them to contract and expel sweat from the gland. The secretory cells are a mix of light and dark staining cells. The light staining cells produce a watery substance similar in composition to an ultrafiltrate of blood, whereas the dark staining cells secrete glycoprotein. The ducts stain darker and have a double layer of cuboidal cells which reabsorb ions and water. The ducts spiral to the surface of the epidermis to release sweat. Sweat evaporates on the surface of the skin to help lower body temperature.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="skin/eccrine_sweat_gland.svg" type="image/svg+xml"></object>
				</figure>
				
				<h3>Apocrine Sweat Gland</h3>
				<p>Apocrine sweat glands occur in the axilla, the areola of the nipple, the labia majora, and the circumanal region. They are much larger than eccrine glands and produce a thicker secretion, which is rich in protein, lipid, carbohydrate, ammonium and other organic compounds. The glands are characterized by a simple cuboidal epithelium and widely dilated lumen that stores the secretory product. 
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="skin/apocrine_sweat_gland.svg" type="image/svg+xml"></object>
				</figure>
				
				<h3>Sebaceous Sweat Gland</h3>
				<p>Sebaceous glands are pear-shaped glands that secrete an oily substance called sebum, which moisturizes and waterproofs hair. They are usually attached to hair follicles near the arrector pili muscle, which causes hair to "stand up". The glands connect with the hair follicle via a short duct called the pilosebaceous canal.
