				</figure>		
				
				<h3>Stratum Basale and Spinosum</h3>
				<p>This image shows the stratum basale and stratum spinosum. The cells of the stratum basale are cuboidal to columnar and undergo mitosis to give rise to all of the keratinocytes in the layers above. Note the dark staining material in the cells of the basale. This is melanin that gives skin its color and absorbs UV-light. Also note the numerous, prickly connections between keratinocytes. These are the connections made by desmosomes. The desmosomes are linked intracellularly to aggregates of intermediate filaments called tonofilaments. Cytokeratin is the primary component of tonofilaments, and keratinocytes in the spinosum synthesize a large amount of cytokeratin (note the prominent nucleoli in the keratinocytes). 
				<figure style="width:100%;">
