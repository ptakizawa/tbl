				</figure>		
				
				<h3>Epidermis</h3>
				<p>The epidermis is a stratified squamous epithelium that contains discrete layers of cells called keratinocytes that are different states of development and differentiation. All keratinocytes derive from stem cells in the deepest layer, the stratum basale, and undergo structural and functional changes as they migrate upward toward the surface of the epidermis. The epidermis is divided into four layers that have different structural appearances:</p>
				<ul>
					<li>The <b>stratum basale</b> is a row of cuboidal to columnar cells resting on the basement memrbane that separates the epidermis from the dermis. Stem cells in this layer give rise to all of the keratinocytes in the layers above. The basale also contain melanocytes that generate pigment for the skin.</li>  
					 <li>After forming in the basal cell layer, keratinocytes migrate upwards into the <b>stratum spinosum</b>. Keratinocytes in all layers are linked to each other via desmosomes which connect intracellularly to intermediate filaments. These connections become especially prominent in the stratum spinosum.</li>
					 <li>The third layer is the <b>stratum granulosum</b>. In this layer, the keratinocytes have become squamous and contain granules of keratohyaline. The contents of the granules will interact with cytokeratin to form keratin.</li>
					 <li>The most superficial layer of the epidermis is the <b>stratum corneum</b>. It consists of flat, keratinized scales that are shed and replaced continuously. This is the layer that includes the final keratin product, which is a combination of cytokeratin and keratohyaline. Keratin is coated in a lipid-rich substance that helps prevent loss of water.</li>
				</ul>
