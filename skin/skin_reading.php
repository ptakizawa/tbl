	        	<h2>Introduction</h2>
				<p>The skin is the largest organ of the body and varies in thickness and composition in different regions of the body. It plays five main roles: protection, thermoregulation, sensation, metabolic functions (vitamin D, adipose metabolism), and sexual attraction.</h2>
				<h2>Layers of the Skin</h2>
				<p>Skin comprises three layers: epidermis, dermis and hypodermis. The <b>epidermis</b> is most superficial and interacts with the external environment. It is a self-regenerating, stratified squamous epithelium that produces a protective layer of keratin. Keratin provides both mechanical support and protection against loss of water. The thickness of the epidermis will vary depending on the its location. The thickest epidermis is usually found in the palms of the hand and soles of the feet.</p>
				<p>Underneath the epidermis is the <b>dermis</b>, a layer rich in collagen. The collagen provides mechanical support by resisting tension in multiple directions.  The dermis also contains blood vessels, nerves and sensory receptors.</p>
				<p>The deepest layer is the <b>hypodermis</b> that is largely composed of adipose tissue but also has large blood vessels. The adipose tissue provides metabolic support and creates an insulating layer to prevent loss of heat.</p>
		  	<p>Note the rete ridges that are down growths of the epidermis into the dermal layer. These generate a stronger bond between the epidermis and dermis and help the skin resist shearing forces. In between rete ridges are dermal papilla, which contain sensory receptors. Also, the stem cells of the epidermis are located in the region above the dermal papilla.</p>
