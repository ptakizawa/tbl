				</figure>
				
				<h3>Stratum Granulosum and Corneum</h3>
				<p>The keratinocytes of the stratum granulosum become more flattened and accumulate numerous dense, dark-staining granules. These are keratohyaline granules that contain proteins which will aggregate cytokeratin to form keratin filaments in the cytoplasm. These cells also produce lipid-rich lamellar bodies that will be secreted to form a lipid layer to prevent water loss. The cells of the stratum corneum are anucleated and have undergone apoptosis.  The dead cells leave layers of keratin. The thickness of this layer varies the most and is primary difference between thick and thin skin.</p>
				<figure style="width:100%;">
					<object height="100%" width="100%" data ="skin/granulosum_corneum.svg" type="image/svg+xml"></object>
				</figure>
				
				<h3>Melanocytes and Langerhans Cells</h3>
				<p>Melanocytes localize to the stratum basale and extend processes between the keratinocytes of the stratum basale and spinosum. Melanocytes produce the pigment melanin that protects cells of the skin against ultraviolet radiation. Melanin is synthesized in membrane-bound organelles called melanosomes that derive from the Golgi. Melanosomes are transported to the ends of the melanocyte processes where neighboring keratinocytes phagocytose the melanosomes. Although melanocytes produce melanin, the cytoplasm of melanocytes is usually pale in comparison to the surrounding basal cells.</p>
				<p>Epidermis also contains cells that have roles in generating an immune response. Langerhans cells are antigen-presenting cells in the immune system. They capture antigen and then enter lymphatic vessels to reach a local lymph node where they present antigen to T-cells.</p>
